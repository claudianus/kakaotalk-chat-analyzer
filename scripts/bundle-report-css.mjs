#!/usr/bin/env node
/**
 * Open Props + kca 레이어 CSS → src/report-styles.ts (빌드 시 인라인용)
 */
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const cssDir = join(root, "src/report/css");
const openPropsPath = join(root, "node_modules/open-props/open-props.min.css");

const layers = [
  "00-kca-tokens.css",
  "01-kca-base.css",
  "02-kca-components.css",
  "03-kca-story.css",
  "04-kca-charts.css",
  "05-kca-ux.css",
  "06-kca-responsive.css",
  "07-kca-oled-glass.css",
  "08-kca-innovation.css",
  "09-kca-llm-deck.css",
];

function readLayer(name) {
  return readFileSync(join(cssDir, name), "utf8");
}

let openProps = "";
try {
  openProps = readFileSync(openPropsPath, "utf8");
} catch {
  console.error("open-props not found — run npm install");
  process.exit(1);
}

const bundle = [
  "/* open-props — https://open-props.style/ (MIT) */",
  openProps,
  "",
  ...layers.flatMap((name) => [`/* ${name} */`, readLayer(name), ""]),
].join("\n");

const outPath = join(root, "src/report-styles.ts");
const escaped = bundle.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");

writeFileSync(
  outPath,
  `/** Generated by scripts/bundle-report-css.mjs — do not edit by hand */\nexport const REPORT_STYLES = \`${escaped}\`;\n`,
  "utf8",
);

const kb = (Buffer.byteLength(bundle) / 1024).toFixed(1);
console.error(`[bundle-report-css] ${kb} KiB → src/report-styles.ts`);
const missing = layers.filter((n) => {
  try {
    readFileSync(join(cssDir, n));
    return false;
  } catch {
    return true;
  }
});
if (missing.length) {
  console.error("missing layers:", missing.join(", "));
  process.exit(1);
}
